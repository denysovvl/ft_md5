#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*con;
	size_t	len1;
	size_t	len2;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	con = ft_strnew(len1 + len2);
	if (!con)
		return (NULL);
	con = ft_strcat(con, s1);
	con = ft_strcat(con, s2);
	return (con);
}
